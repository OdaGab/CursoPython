# ==  igualdade  
# >   maior que 
# <   menor que 
# !=  diferente 
# >=  maior ou igual 
# <=  menor ou igual 

num1 = 1 
num2 = 2 

print("Utilizando o operador == o resultado é -> ", num1 == num2)
print("Utilizando o operador > o resultado é -> ", num1 > num2)
print("Utilizando o operador < o resultado é -> ", num1 < num2)
print("Utilizando o operador != o resultado é -> ", num1 != num2)
print("Utilizando o operador >= o resultado é -> ", num1 >= num2)
print("Utilizando o operador <= o resultado é -> ", num1 <= num2)